package stockphotosmanagerlite.model;

public class Photo {

	private String image;
	private String path;
	private String titleSpanish;
	private String titleEnglish;
	private String descSpanih;
	private String descEnglish;
	private String tagsSpanish;
	private String tagsEnglish;
	private String status;
	private String sentSites;
	private String dateSent;
	private String comments;
	
	public Photo() {
		
	}

	public String getImage() {
		return image;
	}

	public void setImage(String image) {
		this.image = image;
	}

	public String getPath() {
		return path;
	}

	public void setPath(String path) {
		this.path = path;
	}

	public String getTitleSpanish() {
		return titleSpanish;
	}

	public void setTitleSpanish(String titleSpanish) {
		this.titleSpanish = titleSpanish;
	}

	public String getTitleEnglish() {
		return titleEnglish;
	}

	public void setTitleEnglish(String titleEnglish) {
		this.titleEnglish = titleEnglish;
	}

	public String getDescSpanih() {
		return descSpanih;
	}

	public void setDescSpanih(String descSpanih) {
		this.descSpanih = descSpanih;
	}

	public String getDescEnglish() {
		return descEnglish;
	}

	public void setDescEnglish(String descEnglish) {
		this.descEnglish = descEnglish;
	}


	public String getTagsSpanish() {
		return tagsSpanish;
	}

	public void setTagsSpanish(String tagsSpanish) {
		this.tagsSpanish = tagsSpanish;
	}

	public String getTagsEnglish() {
		return tagsEnglish;
	}

	public void setTagsEnglish(String tagsEnglish) {
		this.tagsEnglish = tagsEnglish;
	}

	public String getStatus() {
		return status;
	}

	public void setStatus(String status) {
		this.status = status;
	}

	public String getSentSites() {
		return sentSites;
	}

	public void setSentSites(String sentSites) {
		this.sentSites = sentSites;
	}

	public String getDateSent() {
		return dateSent;
	}

	public void setDateSent(String dateSent) {
		this.dateSent = dateSent;
	}

	public String getComments() {
		return comments;
	}

	public void setComments(String comments) {
		this.comments = comments;
	}
	
	
}
